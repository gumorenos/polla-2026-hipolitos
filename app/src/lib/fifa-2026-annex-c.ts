function parseAnnexCRows(): Record<string, AnnexCAllocation> {
  const allocations: Record<string, AnnexCAllocation> = {};

  for (const row of ANNEX_C_RAW_ROWS.trim().split('\n')) {
    const [key, encodedAssignments] = row.split(':');
    const assignments = encodedAssignments?.match(/3[A-L]/g) as ThirdPlaceGroupCode[] | null;
    if (!key || !assignments || assignments.length !== ANNEX_C_SLOTS.length) {
      throw new Error(`Fila inválida de Annex C: ${row}`);
    }

    allocations[key] = {
      vs1A: assignments[0],
      vs1B: assignments[1],
      vs1D: assignments[2],
      vs1E: assignments[3],
      vs1G: assignments[4],
      vs1I: assignments[5],
      vs1K: assignments[6],
      vs1L: assignments[7],
    };
  }

  return allocations;
}


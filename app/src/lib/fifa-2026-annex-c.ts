// https://digitalhub.fifa.com/m/636f5c9c6f29771f/original/FWC2026_regulations_EN.pdf
export const ANNEX_C_THIRD_PLACE_ALLOCATIONS = parseAnnexCRows();
const ANNEX_C_INTEGRITY_ERRORS = validateAnnexCTable(ANNEX_C_THIRD_PLACE_ALLOCATIONS);
if (ANNEX_C_INTEGRITY_ERRORS.length > 0) {
  throw new Error(`Tabla Annex C inválida: ${ANNEX_C_INTEGRITY_ERRORS[0]}`);
}

export function canonicalThirdPlaceGroups(groups: string[]): string {
  const normalized = groups.map((group) => group.trim().toUpperCase());
  const unique = new Set(normalized);
  if (
    normalized.length !== 8
    || unique.size !== 8
    || normalized.some((group) => !GROUPS.includes(group) || group.length !== 1)
  ) {
    throw new Error('Annex C requiere exactamente ocho grupos únicos entre A y L.');
  }

  return [...unique].sort().join('');
}

export function getAnnexCAllocationForGroups(
  groups: string[],
  allocations: Readonly<Record<string, AnnexCAllocation>> = ANNEX_C_THIRD_PLACE_ALLOCATIONS,
): AnnexCAllocation | null {
  const key = canonicalThirdPlaceGroups(groups);
  return allocations[key] || null;
}

export function resolveThirdPlacePlaceholder(
  placeholder: string,
  slot: AnnexCSlot,
  allocation: AnnexCAllocation,
): ThirdPlaceGroupCode | null {
  const normalized = placeholder.trim().toUpperCase();
  if (!/^3[A-L]+$/.test(normalized)) return null;

  const assigned = allocation[slot];
  return normalized.slice(1).includes(assigned.slice(1)) ? assigned : null;
}

export function validateAnnexCTable(
  allocations: Readonly<Record<string, Partial<Record<AnnexCSlot, string>>>>,
): string[] {
  const errors: string[] = [];
  const keys = Object.keys(allocations);

  if (keys.length !== 495) {
    errors.push(`Annex C debe contener 495 combinaciones y contiene ${keys.length}.`);
  }

  const expectedKeys = new Set(generateGroupCombinations(GROUPS.split(''), 8));
  for (const key of keys) {
    const groups = key.split('');
    if (!/^[A-L]{8}$/.test(key)) errors.push(`Clave inválida: ${key}.`);
    if ([...groups].sort().join('') !== key) errors.push(`Clave no canónica: ${key}.`);
    if (new Set(groups).size !== 8) errors.push(`Clave con grupos duplicados: ${key}.`);
    if (!expectedKeys.has(key)) errors.push(`Combinación no válida: ${key}.`);

    const allocation = allocations[key] || {};
    const allocationKeys = Object.keys(allocation).sort();
    const expectedSlots = [...ANNEX_C_SLOTS].sort();
    if (allocationKeys.join(',') !== expectedSlots.join(',')) {
      errors.push(`Slots inválidos para ${key}.`);
      continue;
    }

    const assigned = ANNEX_C_SLOTS.map((slot) => allocation[slot]);
    if (assigned.some((value) => !value || !/^3[A-L]$/.test(value))) {
      errors.push(`Asignación inválida para ${key}.`);
      continue;
    }

    const assignedGroups = assigned.map((value) => value?.slice(1) || '');
    if (new Set(assignedGroups).size !== 8) {
      errors.push(`Asignación duplicada para ${key}.`);
    }
    if ([...assignedGroups].sort().join('') !== key) {
      errors.push(`La asignación de ${key} no coincide con sus grupos.`);
    }
  }

  for (const expectedKey of expectedKeys) {
    if (!Object.hasOwn(allocations, expectedKey)) {
      errors.push(`Falta la combinación ${expectedKey}.`);
    }
  }

  return errors;
}

function generateGroupCombinations(groups: string[], size: number): string[] {
  const combinations: string[] = [];

  const visit = (start: number, selected: string[]) => {
    if (selected.length === size) {
      combinations.push(selected.join(''));
      return;
    }

    for (let index = start; index <= groups.length - (size - selected.length); index += 1) {
      visit(index + 1, [...selected, groups[index]]);
    }
  };

  visit(0, []);
  return combinations;
}
